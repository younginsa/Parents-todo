export function getDateFnsLocale(locale: AppLocale) {
  return locale === "ko" ? koLocale : enUS;
}

export function getWeekdayLabels(locale: AppLocale) {
  return messages[locale].weekdays_short.split(",");
}

export function getBucketLabel(locale: AppLocale, bucket: string) {
  const map = {
    Today: messages[locale].today,
    Tomorrow: messages[locale].tomorrow,
    "This Week": messages[locale].this_week,
    Later: messages[locale].later,
    "No Date": messages[locale].no_date,
  } as const;

  return map[bucket as keyof typeof map] ?? bucket;
}

export function getCategoryLabel(locale: AppLocale, category: string) {
  const keyMap: Record<string, keyof (typeof messages)["ko"]> = {
    general: "general",
    packing: "packing",
    homework: "homework",
    submission: "submission",
    bring: "bring",
    wear: "wear",
    prepare: "prepare",
    health: "health",
    notice: "notice",
    policy: "policy",
    learning: "learning",
    activity: "activity",
    important: "important",
    event: "event",
    reference: "reference",
  };

  const key = keyMap[category];

  if (!key) return category;

  return messages[locale][key] ?? messages.ko[key];
}

export function useI18n() {
  const { locale } = useAppState();

  return {
    locale,
    t: (key: keyof (typeof messages)["ko"], variables?: Record<string, string>) => {
      let value: string = messages[locale][key] ?? messages.ko[key];

      if (variables) {
        for (const [name, replacement] of Object.entries(variables)) {
          value = value.replace(`{${name}}`, replacement);
        }
      }

      return value;
    },
  };
}
